        self.link = None                # 기사
        self.pic_link = None            # 사진 링크
        self.press_name = None          # 언론사 이름
        self.press_num = None           # 언론사 번호
        self.cd_id = None               # 세부 카테고리(소분류)
        self.c_id = None                # 카테고리(대분류)